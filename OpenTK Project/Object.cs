        public void Load()
        {
            VertexBufferObject = GL.GenBuffer();
            GL.BindBuffer(BufferTarget.ArrayBuffer, VertexBufferObject);
            GL.BufferData(BufferTarget.ArrayBuffer, vertexData.Length * sizeof(float), vertexData, BufferUsageHint.StaticDraw);

            ElementBufferObject = GL.GenBuffer();
            GL.BindBuffer(BufferTarget.ElementArrayBuffer, ElementBufferObject);
            GL.BufferData(BufferTarget.ElementArrayBuffer, indexData.Length * sizeof(uint), indexData, BufferUsageHint.StaticDraw);

            Shader = new Shader("Shaders/shader.vert", "Shaders/shader.frag");
            Shader.Use();

            Texture = new Texture("D:/Projects/VS projects/OpenTK Project/OpenTK Project/Textures/container.png");
            Texture.Use();

            Texture2 = new Texture("D:/Projects/VS projects/OpenTK Project/OpenTK Project/Textures/awesomeface.png");
            Texture2.Use();

            Shader.SetInt("texture0", 0);
            Shader.SetInt("texture1", 1);

            VertexArrayObject = GL.GenVertexArray();
            GL.BindVertexArray(VertexArrayObject);

            GL.BindBuffer(BufferTarget.ArrayBuffer, VertexBufferObject);
            GL.BindBuffer(BufferTarget.ElementArrayBuffer, ElementBufferObject);

            GL.VertexAttribPointer(0, 3, VertexAttribPointerType.Float, false, 5 * sizeof(float), 0);

            int textCoordLocation = Shader.GetAttribLocation("aTexCoord");
            GL.EnableVertexAttribArray(textCoordLocation);
            GL.VertexAttribPointer(textCoordLocation, 2, VertexAttribPointerType.Float, false, 5 * sizeof(float), 3 * sizeof(float));
            GL.EnableVertexAttribArray(0);
        }
